static void load_model(Model *model)
{
    s32 i, size, count;
    code0UnkStruct18 *ptr;
    ModelTextureInfo *info;
    FILE *fp;
    char buf[256];

    if (model->filename == NULL)
    {
        assert(model->model->vertex_info == NULL);
        return;
    }

    sprintf(buf, "assets/models/%s.bin", model->filename);
    fp = fopen(buf, "rb");
    assert(fp != NULL);
    fseek(fp, 0L, SEEK_END);
    size = ftell(fp);
    fseek(fp, 0L, SEEK_SET);
    fread(_pModelsBuffer, size, 1, fp);
    fclose(fp);

    assert(model->model->unk8 == model->model->unkA);
    assert(model->model->vertex_info->size == model->model->vertex_size);

    ptr = (code0UnkStruct18 *)((intptr_t)_pModelsBuffer + model->model->vertex_info->fileoff);
    assert((model->model->vertex_info->size % sizeof(code0UnkStruct18)) == 0);
    count = (model->model->vertex_info->size / sizeof(code0UnkStruct18));
    for (i = 0; i < count; i++)
    {
        ptr->x = SWAP_S16(ptr->x);
        ptr->y = SWAP_S16(ptr->y);
        ptr->z = SWAP_S16(ptr->z);
        ptr++;
    }

    assert(((model->model->cmd_off - model->model->texture_info_off) % sizeof(ModelTextureInfo)) == 0);
    count = ((model->model->cmd_off - model->model->texture_info_off) / sizeof(ModelTextureInfo));
    info = (ModelTextureInfo *)((intptr_t)_pModelsBuffer + model->model->fileoff + model->model->texture_info_off);
    for (i = 0; i < count; i++)
    {
        info->dimx = SWAP_S16(info->dimx);
        info->dimy = SWAP_S16(info->dimy);
        info->offset = SWAP_S32(info->offset);
        info++;
    }
    model->model->fileoff = _pModelsBuffer - gModelsBuffer;
    model->model->vertex_info->fileoff = ((model->model->fileoff + model->model->unk8) + 3) & ~3;
    _pModelsBuffer += size;
}

static void load_models(void)
{
    s32 i;

    _pModelsBuffer = gModelsBuffer;
    for (i = 0; i < ARRAY_COUNT(_models); i++)
        load_model(&_models[i]);
    assert(((intptr_t)_pModelsBuffer - (intptr_t)gModelsBuffer) <= sizeof(gModelsBuffer));
}

